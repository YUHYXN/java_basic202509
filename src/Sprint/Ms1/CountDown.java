package Sprint.Ms1;

public class CountDown {
    public static void main(String[] args) {

        // 10부터 1까지의 숫자를 for문을 사용하여 출력하는 프로그램을 작성

        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }

        //for (초기식; 조건식; 증감식) 구조입니다
        //int i = 10: 반복 변수 i를 10으로 초기화
        //i >= 1: i가 1 이상일 때까지 반복
        //i--: 매 반복마다 i를 1씩 감소

    }
}
