package Sprint.Ms1.Array;

public class ArrayBasic {
    public static void main(String[] args) {
        //크기가 5인 정수 배열을 선언하고,
        //값 {10, 20, 30, 40, 50}으로 초기화한 후 모든 요소를 출력하는 소스를 작성하시오.
        //출력예시
        // 인덱스 0: 10
        // 인덱스 1: 20
        // 인덱스 2: 30
        // 인덱스 3: 40
        // 인덱스 4: 50

        int[] arr = {10, 20, 30, 40, 50};   // int(정수)형 배열 arr 선언 + 초기값 넣기
        // 배열에는 5개의 값이 순서대로 들어 있음
        // 인덱스 0 → 10, 인덱스 1 → 20, 인덱스 2 → 30 ...

        for (int i = 0; i < arr.length; i++) {   // i를 0부터 시작해서 arr.length(배열 길이=5)보다 작을 때까지 반복
            // 즉, i = 0, 1, 2, 3, 4 총 다섯 번 반복
            System.out.println("인덱스 " + i + ": " + arr[i]);
            // "인덱스 i: 값" 형식으로 출력
            // 예: 인덱스 0: 10
            //     인덱스 1: 20
        }

        //실습 해설
        //
        //int[] numbers = {10, 20, 30, 40, 50};: 배열 선언과 동시에 초기화
        //numbers.length: 배열의 길이를 반환합니다
        //배열 인덱스는 0부터 시작합니다
        //numbers[i]로 배열의 i번째 요소에 접근합니다
    }
}
